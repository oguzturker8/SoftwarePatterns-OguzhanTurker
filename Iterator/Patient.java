package Iterator;

public class Patient {
	public int tc;
	public String hospitalName;
	public boolean isCorona;
	public String name;
	
	public Patient(int tc, String hospitalName, String name, boolean isCorona) {
		this.tc = tc;
		this.hospitalName = hospitalName;
		this.isCorona = isCorona;
		this.name = name;
	}
	

}
